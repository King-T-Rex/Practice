#include <stdio.h>

/*
Вычисляет площадь треугольника по трём сторонам (формула Герона):
    - a длина стороны a
    - b длина стороны b
    - c длина стороны c
    - double Площадь треугольника или 0, если треугольник невозможен
*/

double triangle_area(double a, double b, double c) {

    // Проверка: все стороны должны быть положительными
    if (a <= 0 || b <= 0 || c <= 0) {
        printf("Ошибка: все стороны треугольника должны быть положительными.\n");
        return 0;
    }

    // Проверка: треугольник должен существовать
    if (a + b <= c || a + c <= b || b + c <= a) {
        printf("Ошибка: треугольник со сторонами %.2f, %.2f, %.2f невозможен.\n", a, b, c);
        return 0;
    }

    // Полупериметр
    double s = (a + b + c) / 2.0;
    // Вычисление площади по формуле Герона
    return sqrt(s * (s - a) * (s - b) * (s - c));
}